export function extrairValorFinanciamentoDoSlug(slug: string): number | null {
  const match = slug.match(/(\d+)(?:-milhao)?/)
  if (!match) return null
  const num = parseInt(match[1])
  if (slug.includes('milhao')) return num * 1000000
  if (slug.includes('financiamento-') || slug.includes('simulacao-financiamento-')) {
    if (num >= 100 && num <= 900) return num * 1000
  }
  return null
}

export function extrairRendaDoSlug(slug: string): number | null {
  const match = slug.match(/renda-(\d+)/)
  if (!match) return null
  return parseInt(match[1])
}
